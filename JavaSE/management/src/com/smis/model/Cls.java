package com.smis.model;

import java.util.Date;

public class Cls {
    private String clsId;
    private String clsName;
    private String clsAdmin;
    private Date clsTime;
    public String getClsId() {
        return clsId;
    }
    public void setClsId(String clsId) {
        this.clsId = clsId;
    }
    public String getClsName() {
        return clsName;
    }
    public void setClsName(String clsName) {
        this.clsName = clsName;
    }
    public String getClsAdmin() {
        return clsAdmin;
    }
    public void setClsAdmin(String clsAdmin) {
        this.clsAdmin = clsAdmin;
    }
    public Date getClsTime() {
        return clsTime;
    }
    public void setClsTime(Date clsTime) {
        this.clsTime = clsTime;
    }
}
