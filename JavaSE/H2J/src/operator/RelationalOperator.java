package operator;

public class RelationalOperator {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        int c = 5;

        System.out.println(a>b);  //返回 false
        System.out.println(a>=c);  //返回 true

        System.out.println(a==b); //返回false
        System.out.println(a!=b);//返回true

    }
}
